##### Estrutura de dados em Python
##### Objetos do tipo sequência

## Sequência de caracteres

texto = "Aprendendo Python na disciplina de linguagem de programação"

#Permite saber o tamanho da sequência
print(f"Tamanho do texto = {len(texto)}")

#Permite saber se um determinado valor existe na sequência
print(f"Python in texto = {'Python' in texto}")

#Permite contar quantas vezes um valor é encontrado na sequência
print(f"Quantidade de Y no texto = {texto.count('y')}")

#A notação com colchetes permite fatiar a sequência, exibindo somente partes dela. Ex: Exibir do 0 ao 5, excluíndo a partir da posição 6.
print(F"As 5 primeiras letras são: {texto[0:6]}")






































