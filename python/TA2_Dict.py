##### Exemplo 1 - Criação de dicionário vazio, com atribuição posterior de chave e valor
dici_1 = {}
dici_1['nome'] = "João"
dici_1['idade'] = 30
print(dici_1)

##### Exemplo 2 - Criação de dicionário usando um par de elementos na forma de chave : valor
dici_2 = {'nome': 'João', 'idade': 30}
print(dici_2)

##### Exemplo 2 - Criação de dicionário com uma lista de tuplas. Cada tupla representa um par chave : valor
dici_3 = dict([('nome', 'João'), ('idade', 30)])
print(dici_3)


